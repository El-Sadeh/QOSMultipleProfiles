unsigned int 
HumidityUnionPlugin_get_serialized_sample_min_size(
    PRESTypePluginEndpointData endpoint_data,
    RTIBool include_encapsulation,
    RTIEncapsulationId encapsulation_id,
    unsigned int current_alignment)
{
    try {
        unsigned int union_min_size_serialized = 0xffffffff;

        unsigned int initial_alignment = current_alignment;

        unsigned int encapsulation_size = current_alignment;

        if (endpoint_data) {} /* To avoid warnings */ 

        if (include_encapsulation) {

            if (!RTICdrEncapsulation_validEncapsulationId(encapsulation_id)) {
                return 1;
            }
            RTICdrStream_getEncapsulationSize(encapsulation_size);
            encapsulation_size -= current_alignment;
            current_alignment = 0;
            initial_alignment = 0;
        }

        current_alignment +=ResolutionPlugin_get_serialized_sample_min_size(
            endpoint_data,RTI_FALSE,encapsulation_id,current_alignment);
        union_min_size_serialized = RTIOsapiUtility_min( 
            (unsigned int) RTICdrType_getShortMaxSizeSerialized(
                current_alignment), 
                union_min_size_serialized);
        union_min_size_serialized = RTIOsapiUtility_min( 
            (unsigned int) RTICdrType_getFloatMaxSizeSerialized(
                current_alignment), 
                union_min_size_serialized);

        if (include_encapsulation) {
            current_alignment += encapsulation_size;
        }
        return  union_min_size_serialized + current_alignment - initial_alignment;
    } catch (...) {
        return 0;
    }
}

/* Returns the size of the sample in its serialized form (in bytes).
* It can also be an estimation in excess of the real buffer needed 
* during a call to the serialize() function.
* The value reported does not have to include the space for the
* encapsulation flags.
*/
unsigned int
HumidityUnionPlugin_get_serialized_sample_size(
    PRESTypePluginEndpointData endpoint_data,
    RTIBool include_encapsulation,
    RTIEncapsulationId encapsulation_id,
    unsigned int current_alignment,
    const HumidityUnion * sample) 
{
    try {  
        unsigned int initial_alignment = current_alignment;

        unsigned int encapsulation_size = current_alignment;
        struct PRESTypePluginDefaultEndpointData epd;   

        if (sample==NULL) {
            return 0;
        }
        if (endpoint_data == NULL) {
            endpoint_data = (PRESTypePluginEndpointData) &epd;
            PRESTypePluginDefaultEndpointData_setBaseAlignment(
                endpoint_data,
                current_alignment);        
        }

        if (include_encapsulation) {

            if (!RTICdrEncapsulation_validEncapsulationId(encapsulation_id)) {
                return 1;
            }
            RTICdrStream_getEncapsulationSize(encapsulation_size);
            encapsulation_size -= current_alignment;
            current_alignment = 0;
            initial_alignment = 0;
            PRESTypePluginDefaultEndpointData_setBaseAlignment(
                endpoint_data,
                current_alignment);
        }

        current_alignment += ResolutionPlugin_get_serialized_sample_size(
            endpoint_data,RTI_FALSE, encapsulation_id,
            current_alignment, &sample->_d());
        switch(rti::topic::cdr::integer_case(sample->_d())) {
            case (Resolution::LOW):
                {  

                    current_alignment += RTICdrType_getShortMaxSizeSerialized(
                        PRESTypePluginDefaultEndpointData_getAlignment(
                            endpoint_data, current_alignment));
            }break ;
            case (Resolution::HIGH):
                {  

                    current_alignment += RTICdrType_getFloatMaxSizeSerialized(
                        PRESTypePluginDefaultEndpointData_getAlignment(
                            endpoint_data, current_alignment));
            }break ;
        }

        if (include_encapsulation) {
            current_alignment += encapsulation_size;
        }
        return current_alignment - initial_alignment;
    } catch (...) {
        return RTI_FALSE;
    }    
}

